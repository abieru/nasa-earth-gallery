// Determine initial language
let currentLang = localStorage.getItem('lang');
if (!currentLang || !translations[currentLang]) {
  const browserLang = navigator.language || navigator.userLanguage || 'en';
  const baseLang = browserLang.split('-')[0];
  if (translations[browserLang]) {
    currentLang = browserLang;
  } else if (translations[baseLang]) {
    currentLang = baseLang;
  } else {
    currentLang = 'en';
  }
}

function t(key, vars = {}) {
  let str = translations[currentLang]?.[key] ?? translations['en'][key] ?? key;
  Object.keys(vars).forEach(k => {
    str = str.replace(new RegExp(`\\{${k}\\}`, 'g'), vars[k]);
  });
  return str;
}

function getLocale() {
  return currentLang === 'es-VE' ? 'es-VE' : currentLang === 'pt-BR' ? 'pt-BR' : 'en-US';
}

function setLang(lang) {
  if (!translations[lang]) return;
  currentLang = lang;
  localStorage.setItem('lang', lang);
  updatePageText();
}

function updatePageText() {
  // Update page language attribute
  document.documentElement.lang = currentLang;

  // Update static elements
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.dataset.i18n;
    const text = t(key);
    if (text.includes('<')) {
      el.innerHTML = text;
    } else {
      el.textContent = text;
    }
  });
  document.title = t('title');

  // Update dynamic content if gallery has been rendered
  if (typeof currentData !== 'undefined' && typeof renderGallery === 'function' && currentData.length > 0) {
    renderGallery(currentData);
    const modal = document.getElementById('modal');
    if (modal && !modal.classList.contains('hidden') && typeof openModal === 'function') {
      const idx = parseInt(modal.dataset.index || '0');
      openModal(idx);
    }
    const infoDiv = document.getElementById('info');
    if (infoDiv && !infoDiv.classList.contains('hidden') && typeof showInfo === 'function' && typeof formatDate === 'function') {
      const date = currentData[0].date.split(' ')[0];
      showInfo(t('showingImages', { count: currentData.length, date: formatDate(date) }));
    }
  }
}
